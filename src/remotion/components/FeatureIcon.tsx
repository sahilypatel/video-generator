interface FeatureIconProps {
  icon: string;
  accentColor: string;
  size?: number;
  lottieUrl?: string;
}

export const FeatureIcon: React.FC<FeatureIconProps> = ({
  icon,
  accentColor,
  size = 120,
  lottieUrl,
}) => {
  if (lottieUrl) {
    const { LottieIcon } = require("./LottieIcon");
    return (
      <LottieIcon url={lottieUrl} size={size} accentColor={accentColor} />
    );
  }

  const svgIcon = ICON_MAP[icon];

  const iconContent = svgIcon ? (
    <div
      style={{
        width: size * 0.48,
        height: size * 0.48,
        color: "white",
        display: "flex",
        alignItems: "center",
        justifyContent: "center",
      }}
    >
      {svgIcon}
    </div>
  ) : (
    <span style={{ fontSize: size * 0.44, lineHeight: 1 }}>{icon}</span>
  );

  return (
    <div
      style={{
        width: size,
        height: size,
        borderRadius: size * 0.27,
        background: `linear-gradient(135deg, ${accentColor}22 0%, ${accentColor}42 100%)`,
        border: `1px solid ${accentColor}50`,
        display: "flex",
        alignItems: "center",
        justifyContent: "center",
        position: "relative",
        boxShadow: `0 0 50px ${accentColor}28, inset 0 1px 0 rgba(255,255,255,0.12), inset 0 -1px 0 rgba(0,0,0,0.12)`,
      }}
    >
      {/* Inner highlight ring */}
      <div
        style={{
          position: "absolute",
          inset: 1,
          borderRadius: size * 0.25,
          background: `linear-gradient(145deg, rgba(255,255,255,0.08) 0%, transparent 50%)`,
          pointerEvents: "none",
        }}
      />
      {iconContent}
    </div>
  );
};
